valor_um = int(input("Digite o primeiro valor: "))
valor_dois = int(input("Digite o segundo valor: "))

adicao = valor_um + valor_dois
subtracao = valor_um - valor_dois
multiplicacao = valor_um * valor_dois
divisao = valor_um / valor_dois

print(f"Adição: {adicao}\n Subtração: {subtracao}\n Multiplicação: {multiplicacao}\n Divisão: {divisao}\n")